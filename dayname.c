#include<stdio.h>
void main()
{
    int num;
    printf("Enter the num");
    scanf("%d",&num);
    if(num==1)
    {
     printf("Monday");
    }
    else  if(num==2)
    {
     printf("tuesday");
    }
    else  if(num==3)
    {
     printf("Wednesday");
    }
    else  if(num==4)
    {
     printf("thursday");
    }
    else  if(num==5)
    {
     printf("friday");
    }
    else  if(num==6)
    {
     printf("saturday");
    }
    else  if(num==7)
    {
     printf("Sunday");
    }
}
